WITH RECURSIVE Ιεραρχία_Εποπτείας AS (
    -- Βάση: όλοι οι ιατροί
    SELECT 
        Ι.ΑΜΚΑ_ΙΑΤΡΟΥ AS ΑΜΚΑ_ΑΡΧΙΚΟΥ,
        Ι.ΑΜΚΑ_ΙΑΤΡΟΥ,
        Π.ΟΝΟΜΑ,
        Π.ΕΠΩΝΥΜΟ,
        Ι.ΒΑΘΜΙΔΑ,
        Ι.ΑΜΚΑ_ΕΠΟΠΤΗ,
        0 AS ΕΠΙΠΕΔΟ
    FROM ΙΑΤΡΟΙ Ι
    JOIN ΠΡΟΣΩΠΙΚΟ Π ON Ι.ΑΜΚΑ_ΙΑΤΡΟΥ = Π.ΑΜΚΑ_ΠΡΟΣΩΠΙΚΟΥ
    WHERE Ι.ΑΜΚΑ_ΕΠΟΠΤΗ IS NOT NULL  -- μόνο όσοι έχουν επόπτη

    UNION ALL

    -- Αναδρομή: ανέβα στον επόπτη
    SELECT 
        Ι.ΑΜΚΑ_ΑΡΧΙΚΟΥ,
        Ε.ΑΜΚΑ_ΙΑΤΡΟΥ,
        ΠΕ.ΟΝΟΜΑ,
        ΠΕ.ΕΠΩΝΥΜΟ,
        Ε.ΒΑΘΜΙΔΑ,
        Ε.ΑΜΚΑ_ΕΠΟΠΤΗ,
        Ι.ΕΠΙΠΕΔΟ + 1
    FROM ΙΑΤΡΟΙ Ε
    JOIN ΠΡΟΣΩΠΙΚΟ ΠΕ ON Ε.ΑΜΚΑ_ΙΑΤΡΟΥ = ΠΕ.ΑΜΚΑ_ΠΡΟΣΩΠΙΚΟΥ
    JOIN Ιεραρχία_Εποπτείας Ι ON Ε.ΑΜΚΑ_ΙΑΤΡΟΥ = Ι.ΑΜΚΑ_ΕΠΟΠΤΗ
    WHERE Ι.ΑΜΚΑ_ΕΠΟΠΤΗ IS NOT NULL
)
SELECT 
    ΑΜΚΑ_ΑΡΧΙΚΟΥ AS ΑΜΚΑ_ΙΑΤΡΟΥ_ΒΑΣΗ,
    ΑΜΚΑ_ΙΑΤΡΟΥ AS ΑΜΚΑ_ΕΠΟΠΤΗ,
    ΟΝΟΜΑ,
    ΕΠΩΝΥΜΟ,
    ΒΑΘΜΙΔΑ,
    ΕΠΙΠΕΔΟ
FROM Ιεραρχία_Εποπτείας
WHERE ΕΠΙΠΕΔΟ > 0  -- μόνο επόπτες, όχι ο ίδιος
ORDER BY ΑΜΚΑ_ΑΡΧΙΚΟΥ, ΕΠΙΠΕΔΟ;