SELECT
    π.ΑΜΚΑ_ΠΡΟΣΩΠΙΚΟΥ                               AS ΑΜΚΑ_ΙΑΤΡΟΥ,
    π.ΟΝΟΜΑ,
    π.ΕΠΩΝΥΜΟ,
    π.ΗΛΙΚΙΑ,
    ι.ΕΙΔΙΚΟΤΗΤΑ,
    ι.ΒΑΘΜΙΔΑ,
    COUNT(*)                                         AS ΑΡΙΘΜΟΣ_ΧΕΙΡΟΥΡΓΕΙΩΝ
FROM ΠΡΟΣΩΠΙΚΟ π
JOIN ΙΑΤΡΟΙ ι
    ON π.ΑΜΚΑ_ΠΡΟΣΩΠΙΚΟΥ = ι.ΑΜΚΑ_ΙΑΤΡΟΥ
JOIN ΙΑΤΡΙΚΕΣ_ΠΡΑΞΕΙΣ_ΑΣΘΕΝΗ ιπα
    ON ι.ΑΜΚΑ_ΙΑΤΡΟΥ = ιπα.ΑΜΚΑ_ΙΑΤΡΟΥ
JOIN ΙΑΤΡΙΚΕΣ_ΠΡΑΞΕΙΣ ιπ
    ON ιπα.ΚΩΔΙΚΟΣ_ΠΡΑΞΗΣ = ιπ.ΚΩΔΙΚΟΣ_ΠΡΑΞΗΣ
WHERE π.ΗΛΙΚΙΑ < 35
  AND ιπ.ΚΑΤΗΓΟΡΙΑ = 'ΠΡΑΞΕΙΣ ΧΕΙΡΟΥΡΓΙΚΕΣ – ΕΠΕΜΒΑΤΙΚΕΣ – ΕΝΔΟΣΚΟΠΙΚΕΣ'
GROUP BY
    π.ΑΜΚΑ_ΠΡΟΣΩΠΙΚΟΥ,
    π.ΟΝΟΜΑ,
    π.ΕΠΩΝΥΜΟ,
    π.ΗΛΙΚΙΑ,
    ι.ΕΙΔΙΚΟΤΗΤΑ,
    ι.ΒΑΘΜΙΔΑ
ORDER BY ΑΡΙΘΜΟΣ_ΧΕΙΡΟΥΡΓΕΙΩΝ DESC;
