SELECT 
    Β.ΟΝΟΜΑ_ΤΜΗΜΑΤΟΣ,
    Β.ΚΩΔΙΚΟΣ_ΒΑΡΔΙΑΣ,
    Β.ΗΜΕΡΟΜΗΝΙΑ,
    Β.ΤΥΠΟΣ AS ΤΥΠΟΣ_ΒΑΡΔΙΑΣ,
    Π.ΤΥΠΟΣ_ΠΡΟΣΩΠΙΚΟΥ,
    COALESCE(Ι.ΕΙΔΙΚΟΤΗΤΑ, Ν.ΒΑΘΜΙΔΑ, Δ.ΡΟΛΟΣ) AS ΥΠΟΚΛΑΣΗ_ΠΡΟΣΩΠΙΚΟΥ,
    COUNT(Π.ΑΜΚΑ_ΠΡΟΣΩΠΙΚΟΥ) AS ΑΡΙΘΜΟΣ_ΠΡΟΣΩΠΙΚΟΥ
FROM ΒΑΡΔΙΕΣ Β
JOIN ΟΜΑΔΑ_ΕΦΗΜΕΡΙΑΣ ΟΕ ON Β.ΚΩΔΙΚΟΣ_ΒΑΡΔΙΑΣ = ΟΕ.ΚΩΔΙΚΟΣ_ΒΑΡΔΙΑΣ
JOIN ΠΡΟΣΩΠΙΚΟ Π ON ΟΕ.ΑΜΚΑ_ΠΡΟΣΩΠΙΚΟΥ = Π.ΑΜΚΑ_ΠΡΟΣΩΠΙΚΟΥ
LEFT JOIN ΙΑΤΡΟΙ Ι ON Π.ΑΜΚΑ_ΠΡΟΣΩΠΙΚΟΥ = Ι.ΑΜΚΑ_ΙΑΤΡΟΥ
LEFT JOIN ΝΟΣΗΛΕΥΤΕΣ Ν ON Π.ΑΜΚΑ_ΠΡΟΣΩΠΙΚΟΥ = Ν.ΑΜΚΑ_ΝΟΣΗΛΕΥΤΗ
LEFT JOIN ΔΙΟΙΚΗΤΙΚΟ_ΠΡΟΣΩΠΙΚΟ Δ ON Π.ΑΜΚΑ_ΠΡΟΣΩΠΙΚΟΥ = Δ.ΑΜΚΑ_ΔΙΟΙΚΗΤΙΚΟΥ
WHERE Β.ΗΜΕΡΟΜΗΝΙΑ BETWEEN '2026-04-20' AND '2026-04-26'
GROUP BY 
    Β.ΟΝΟΜΑ_ΤΜΗΜΑΤΟΣ, Β.ΚΩΔΙΚΟΣ_ΒΑΡΔΙΑΣ,
    Β.ΗΜΕΡΟΜΗΝΙΑ, Β.ΤΥΠΟΣ,
    Π.ΤΥΠΟΣ_ΠΡΟΣΩΠΙΚΟΥ, ΥΠΟΚΛΑΣΗ_ΠΡΟΣΩΠΙΚΟΥ
ORDER BY 
    Β.ΗΜΕΡΟΜΗΝΙΑ,
    Β.ΟΝΟΜΑ_ΤΜΗΜΑΤΟΣ,
    CASE Β.ΤΥΠΟΣ 
        WHEN 'Πρωινή' THEN 1 
        WHEN 'Απογευματινή' THEN 2 
        WHEN 'Νυχτερινή' THEN 3 
    END,
    Π.ΤΥΠΟΣ_ΠΡΟΣΩΠΙΚΟΥ,
    ΥΠΟΚΛΑΣΗ_ΠΡΟΣΩΠΙΚΟΥ
LIMIT 50;