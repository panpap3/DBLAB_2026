WITH Ουσίες_Ανά_Νοσηλεία AS (
    SELECT DISTINCT
        Σ.ΚΩΔΙΚΟΣ_ΝΟΣΗΛΕΙΑΣ,
        Φ.ΔΡΑΣΤΙΚΗ_ΟΥΣΙΑ
    FROM ΣΥΝΤΑΓΟΓΡΑΦΗΣΗ Σ                         -- idx_syntago_nosilia
    JOIN ΦΑΡΜΑΚΑ_ΑΡΘΡΟ_57 Φ ON Σ.ΦΑΡΜΑΚΟ_ID = Φ.ΦΑΡΜΑΚΟ_ID  -- idx_fa57_ousia
)
SELECT 
    Ο1.ΔΡΑΣΤΙΚΗ_ΟΥΣΙΑ AS ΔΡΑΣΤΙΚΗ_ΟΥΣΙΑ_1,
    Ο2.ΔΡΑΣΤΙΚΗ_ΟΥΣΙΑ AS ΔΡΑΣΤΙΚΗ_ΟΥΣΙΑ_2,
    COUNT(*) AS ΣΥΧΝΟΤΗΤΑ_ΕΜΦΑΝΙΣΗΣ
FROM Ουσίες_Ανά_Νοσηλεία Ο1
JOIN Ουσίες_Ανά_Νοσηλεία Ο2
    ON Ο1.ΚΩΔΙΚΟΣ_ΝΟΣΗΛΕΙΑΣ = Ο2.ΚΩΔΙΚΟΣ_ΝΟΣΗΛΕΙΑΣ
    AND Ο1.ΔΡΑΣΤΙΚΗ_ΟΥΣΙΑ < Ο2.ΔΡΑΣΤΙΚΗ_ΟΥΣΙΑ
GROUP BY Ο1.ΔΡΑΣΤΙΚΗ_ΟΥΣΙΑ, Ο2.ΔΡΑΣΤΙΚΗ_ΟΥΣΙΑ
ORDER BY ΣΥΧΝΟΤΗΤΑ_ΕΜΦΑΝΙΣΗΣ DESC
LIMIT 3;                                          -- TOP-3 per εκφώνηση