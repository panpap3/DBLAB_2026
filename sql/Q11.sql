WITH Χειρουργεία_Ιατρών AS (
    SELECT 
        Ι.ΑΜΚΑ_ΙΑΤΡΟΥ,
        Π.ΟΝΟΜΑ,
        Π.ΕΠΩΝΥΜΟ,
        COUNT(CASE WHEN ΙΠ.ΚΑΤΗΓΟΡΙΑ = 'ΠΡΑΞΕΙΣ ΧΕΙΡΟΥΡΓΙΚΕΣ – ΕΠΕΜΒΑΤΙΚΕΣ – ΕΝΔΟΣΚΟΠΙΚΕΣ' 
                   THEN 1 END) AS ΑΡΙΘΜΟΣ_ΧΕΙΡΟΥΡΓΕΙΩΝ
    FROM ΙΑΤΡΟΙ Ι
    JOIN ΠΡΟΣΩΠΙΚΟ Π ON Ι.ΑΜΚΑ_ΙΑΤΡΟΥ = Π.ΑΜΚΑ_ΠΡΟΣΩΠΙΚΟΥ
    LEFT JOIN ΙΑΤΡΙΚΕΣ_ΠΡΑΞΕΙΣ_ΑΣΘΕΝΗ ΙΠΑ
        ON Ι.ΑΜΚΑ_ΙΑΤΡΟΥ = ΙΠΑ.ΑΜΚΑ_ΙΑΤΡΟΥ
        AND EXTRACT(YEAR FROM ΙΠΑ.ΗΜΕΡΟΜΗΝΙΑ_ΩΡΑ) = EXTRACT(YEAR FROM CURRENT_DATE)
    LEFT JOIN ΙΑΤΡΙΚΕΣ_ΠΡΑΞΕΙΣ ΙΠ ON ΙΠΑ.ΚΩΔΙΚΟΣ_ΠΡΑΞΗΣ = ΙΠ.ΚΩΔΙΚΟΣ_ΠΡΑΞΗΣ
    GROUP BY Ι.ΑΜΚΑ_ΙΑΤΡΟΥ, Π.ΟΝΟΜΑ, Π.ΕΠΩΝΥΜΟ
)
SELECT 
    ΑΜΚΑ_ΙΑΤΡΟΥ,
    ΟΝΟΜΑ,
    ΕΠΩΝΥΜΟ,
    ΑΡΙΘΜΟΣ_ΧΕΙΡΟΥΡΓΕΙΩΝ
FROM Χειρουργεία_Ιατρών
WHERE ΑΡΙΘΜΟΣ_ΧΕΙΡΟΥΡΓΕΙΩΝ <= (
    SELECT MAX(ΑΡΙΘΜΟΣ_ΧΕΙΡΟΥΡΓΕΙΩΝ) FROM Χειρουργεία_Ιατρών
) - 5
ORDER BY ΑΡΙΘΜΟΣ_ΧΕΙΡΟΥΡΓΕΙΩΝ DESC; 