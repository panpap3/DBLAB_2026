SELECT 
    Ν.ΟΝΟΜΑ_ΤΜΗΜΑΤΟΣ,
    EXTRACT(YEAR FROM Ν.ΗΜΕΡΟΜΗΝΙΑ_ΕΙΣΑΓΩΓΗΣ) AS ΕΤΟΣ,
    Κ.ΚΩΔΙΚΟΣ_ΚΕΝ,
    COALESCE(ΑΦ.ΟΝΟΜΑ_ΦΟΡΕΑ, 'ΑΝΑΣΦΑΛΙΣΤΟΣ') AS ΑΣΦΑΛΙΣΤΙΚΟΣ_ΦΟΡΕΑΣ,
    COUNT(Ν.ΚΩΔΙΚΟΣ_ΝΟΣΗΛΕΙΑΣ) AS ΑΡΙΘΜΟΣ_ΝΟΣΗΛΕΙΩΝ,
    SUM(Κ.ΚΟΣΤΟΣ_ΑΝΑ_ΚΕΝ) AS ΒΑΣΙΚΟ_ΚΟΣΤΟΣ_ΚΕΝ,
    SUM(GREATEST(0, Ν.ΚΟΣΤΟΣ_ΝΟΣΗΛΕΙΑΣ - Κ.ΚΟΣΤΟΣ_ΑΝΑ_ΚΕΝ)) AS ΠΡΟΣΘΕΤΗ_ΧΡΕΩΣΗ_ΜΔΝ,
    SUM(Ν.ΚΟΣΤΟΣ_ΝΟΣΗΛΕΙΑΣ) AS ΣΥΝΟΛΙΚΑ_ΕΣΟΔΑ
FROM ΝΟΣΗΛΕΙΑ Ν                                  -- idx_nosilia_tmima, idx_nosilia_eisagogi, idx_nosilia_exodos
JOIN ΚΟΣΤΟΛΟΓΗΣΗ Κ ON Ν.ΚΕΝ_ID = Κ.ΚΕΝ_ID
JOIN ΑΣΘΕΝΕΙΣ Α ON Ν.ΑΜΚΑ_ΑΣΘΕΝΗ = Α.ΑΜΚΑ_ΑΣΘΕΝΗ
LEFT JOIN ΑΣΦΑΛΙΣΤΙΚΟΣ_ΦΟΡΕΑΣ ΑΦ ON Α.ΑΜΚΑ_ΑΣΘΕΝΗ = ΑΦ.ΑΜΚΑ_ΑΣΘΕΝΗ  -- idx_asfal_asthenis
WHERE Ν.ΗΜΕΡΟΜΗΝΙΑ_ΕΞΟΔΟΥ IS NOT NULL
GROUP BY 
    Ν.ΟΝΟΜΑ_ΤΜΗΜΑΤΟΣ,
    EXTRACT(YEAR FROM Ν.ΗΜΕΡΟΜΗΝΙΑ_ΕΙΣΑΓΩΓΗΣ),
    Κ.ΚΩΔΙΚΟΣ_ΚΕΝ,
    ΑΦ.ΟΝΟΜΑ_ΦΟΡΕΑ
ORDER BY ΕΤΟΣ DESC, ΣΥΝΟΛΙΚΑ_ΕΣΟΔΑ DESC
LIMIT 100;