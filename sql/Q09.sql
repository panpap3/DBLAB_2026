WITH Συνολικές_Ημέρες_Ασθενών AS (
    SELECT 
        ΑΜΚΑ_ΑΣΘΕΝΗ, 
        EXTRACT(YEAR FROM ΗΜΕΡΟΜΗΝΙΑ_ΕΙΣΑΓΩΓΗΣ) AS ΕΤΟΣ,
        SUM(DATEDIFF(ΗΜΕΡΟΜΗΝΙΑ_ΕΞΟΔΟΥ, ΗΜΕΡΟΜΗΝΙΑ_ΕΙΣΑΓΩΓΗΣ)) AS ΣΥΝΟΛΙΚΕΣ_ΗΜΕΡΕΣ
    FROM ΝΟΣΗΛΕΙΑ
    WHERE ΗΜΕΡΟΜΗΝΙΑ_ΕΞΟΔΟΥ IS NOT NULL
    GROUP BY 
        ΑΜΚΑ_ΑΣΘΕΝΗ, 
        EXTRACT(YEAR FROM ΗΜΕΡΟΜΗΝΙΑ_ΕΙΣΑΓΩΓΗΣ)
    HAVING SUM(DATEDIFF(ΗΜΕΡΟΜΗΝΙΑ_ΕΞΟΔΟΥ, ΗΜΕΡΟΜΗΝΙΑ_ΕΙΣΑΓΩΓΗΣ)) > 15
)
SELECT 
    Σ1.ΑΜΚΑ_ΑΣΘΕΝΗ AS ΑΣΘΕΝΗΣ_1, 
    Σ2.ΑΜΚΑ_ΑΣΘΕΝΗ AS ΑΣΘΕΝΗΣ_2, 
    Σ1.ΕΤΟΣ, 
    Σ1.ΣΥΝΟΛΙΚΕΣ_ΗΜΕΡΕΣ
FROM Συνολικές_Ημέρες_Ασθενών Σ1
JOIN Συνολικές_Ημέρες_Ασθενών Σ2 
    ON Σ1.ΕΤΟΣ = Σ2.ΕΤΟΣ 
    AND Σ1.ΣΥΝΟΛΙΚΕΣ_ΗΜΕΡΕΣ = Σ2.ΣΥΝΟΛΙΚΕΣ_ΗΜΕΡΕΣ 
    AND Σ1.ΑΜΚΑ_ΑΣΘΕΝΗ < Σ2.ΑΜΚΑ_ΑΣΘΕΝΗ
ORDER BY Σ1.ΕΤΟΣ DESC, Σ1.ΣΥΝΟΛΙΚΕΣ_ΗΜΕΡΕΣ DESC
LIMIT 50;