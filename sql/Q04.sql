-- (α) Κανονικό query με EXPLAIN
EXPLAIN
SELECT 
    Π.ΟΝΟΜΑ, Π.ΕΠΩΝΥΜΟ, Ι.ΑΜΚΑ_ΙΑΤΡΟΥ,
    ROUND(AVG(ΑΙ.ΠΟΙΟΤΗΤΑ_ΙΑΤΡΙΚΗΣ_ΦΡΟΝΤΙΔΑΣ), 2) AS ΜΕΣΟΣ_ΟΡΟΣ_ΑΞΙΟΛΟΓΗΣΗΣ_ΙΑΤΡΟΥ,
    ROUND(AVG(ΑΝ.ΣΥΝΟΛΙΚΗ_ΕΜΠΕΙΡΙΑ), 2) AS ΜΕΣΟΣ_ΟΡΟΣ_ΣΥΝΟΛΙΚΗΣ_ΕΜΠΕΙΡΙΑΣ
FROM ΙΑΤΡΟΙ Ι
JOIN ΠΡΟΣΩΠΙΚΟ Π ON Ι.ΑΜΚΑ_ΙΑΤΡΟΥ = Π.ΑΜΚΑ_ΠΡΟΣΩΠΙΚΟΥ
LEFT JOIN ΑΞΙΟΛΟΓΗΣΗ_ΙΑΤΡΟΥ ΑΙ ON Ι.ΑΜΚΑ_ΙΑΤΡΟΥ = ΑΙ.ΑΜΚΑ_ΙΑΤΡΟΥ
LEFT JOIN ΝΟΣΗΛΕΙΑ Ν ON ΑΙ.ΚΩΔΙΚΟΣ_ΝΟΣΗΛΕΙΑΣ = Ν.ΚΩΔΙΚΟΣ_ΝΟΣΗΛΕΙΑΣ
LEFT JOIN ΑΞΙΟΛΟΓΗΣΗ_ΝΟΣΗΛΕΙΑΣ ΑΝ ON Ν.ΚΩΔΙΚΟΣ_ΝΟΣΗΛΕΙΑΣ = ΑΝ.ΚΩΔΙΚΟΣ_ΝΟΣΗΛΕΙΑΣ
    AND ΑΙ.ΑΜΚΑ_ΑΣΘΕΝΗ = ΑΝ.ΑΜΚΑ_ΑΣΘΕΝΗ
WHERE Ι.ΑΜΚΑ_ΙΑΤΡΟΥ = '16059786000'
GROUP BY Π.ΟΝΟΜΑ, Π.ΕΠΩΝΥΜΟ, Ι.ΑΜΚΑ_ΙΑΤΡΟΥ;

