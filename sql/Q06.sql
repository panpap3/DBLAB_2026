-- (α) Κανονικό query με EXPLAIN
EXPLAIN
SELECT 
    Α.ΑΜΚΑ_ΑΣΘΕΝΗ, Α.ΟΝΟΜΑ, Α.ΕΠΩΝΥΜΟ,
    Ν.ΚΩΔΙΚΟΣ_ΝΟΣΗΛΕΙΑΣ, Ν.ΗΜΕΡΟΜΗΝΙΑ_ΕΙΣΑΓΩΓΗΣ,
    Ν.ΗΜΕΡΟΜΗΝΙΑ_ΕΞΟΔΟΥ, Ν.ΚΟΣΤΟΣ_ΝΟΣΗΛΕΙΑΣ,
    GROUP_CONCAT(DISTINCT CONCAT(Δ.ΤΥΠΟΣ, ': ', Κ.ΚΩΔΙΚΟΣ_ICD10, ' - ', Κ.ΠΕΡΙΓΡΑΦΗ) SEPARATOR ' | ') AS ΔΙΑΓΝΩΣΕΙΣ,
    ROUND(AVG(ΑΝ.ΣΥΝΟΛΙΚΗ_ΕΜΠΕΙΡΙΑ), 2) AS ΜΕΣΟΣ_ΟΡΟΣ_ΑΞΙΟΛΟΓΗΣΗΣ
FROM ΑΣΘΕΝΕΙΣ Α
JOIN ΝΟΣΗΛΕΙΑ Ν ON Α.ΑΜΚΑ_ΑΣΘΕΝΗ = Ν.ΑΜΚΑ_ΑΣΘΕΝΗ
LEFT JOIN ΔΙΑΓΝΩΣΕΙΣ_ΝΟΣΗΛΕΙΑΣ Δ ON Ν.ΚΩΔΙΚΟΣ_ΝΟΣΗΛΕΙΑΣ = Δ.ΚΩΔΙΚΟΣ_ΝΟΣΗΛΕΙΑΣ
LEFT JOIN ΚΩΔΙΚΟΙ_ICD10 Κ ON Δ.ΚΩΔΙΚΟΣ_ICD10 = Κ.ΚΩΔΙΚΟΣ_ICD10
LEFT JOIN ΑΞΙΟΛΟΓΗΣΗ_ΝΟΣΗΛΕΙΑΣ ΑΝ ON Ν.ΚΩΔΙΚΟΣ_ΝΟΣΗΛΕΙΑΣ = ΑΝ.ΚΩΔΙΚΟΣ_ΝΟΣΗΛΕΙΑΣ
    AND Α.ΑΜΚΑ_ΑΣΘΕΝΗ = ΑΝ.ΑΜΚΑ_ΑΣΘΕΝΗ
WHERE Α.ΑΜΚΑ_ΑΣΘΕΝΗ = '22033194683'
GROUP BY Α.ΑΜΚΑ_ΑΣΘΕΝΗ, Α.ΟΝΟΜΑ, Α.ΕΠΩΝΥΜΟ,
    Ν.ΚΩΔΙΚΟΣ_ΝΟΣΗΛΕΙΑΣ, Ν.ΗΜΕΡΟΜΗΝΙΑ_ΕΙΣΑΓΩΓΗΣ,
    Ν.ΗΜΕΡΟΜΗΝΙΑ_ΕΞΟΔΟΥ, Ν.ΚΟΣΤΟΣ_ΝΟΣΗΛΕΙΑΣ
ORDER BY Ν.ΗΜΕΡΟΜΗΝΙΑ_ΕΙΣΑΓΩΓΗΣ DESC;

