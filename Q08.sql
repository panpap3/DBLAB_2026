WITH Προσωπικό_Ανά_Τμήμα AS (
    SELECT ΑΜΚΑ_ΙΑΤΡΟΥ AS ΑΜΚΑ, ΟΝΟΜΑ_ΤΜΗΜΑΤΟΣ FROM ΙΑΤΡΟΙ_ΤΜΗΜΑΤΑ
    UNION
    SELECT ΑΜΚΑ_ΝΟΣΗΛΕΥΤΗ, ΟΝΟΜΑ_ΤΜΗΜΑΤΟΣ FROM ΝΟΣΗΛΕΥΤΕΣ
    UNION
    SELECT ΑΜΚΑ_ΔΙΟΙΚΗΤΙΚΟΥ, ΟΝΟΜΑ_ΤΜΗΜΑΤΟΣ FROM ΔΙΟΙΚΗΤΙΚΟ_ΠΡΟΣΩΠΙΚΟ
)
SELECT 
    Π.ΑΜΚΑ_ΠΡΟΣΩΠΙΚΟΥ,
    Π.ΟΝΟΜΑ,
    Π.ΕΠΩΝΥΜΟ,
    Π.ΤΥΠΟΣ_ΠΡΟΣΩΠΙΚΟΥ
FROM ΠΡΟΣΩΠΙΚΟ Π
JOIN Προσωπικό_Ανά_Τμήμα ΠΑΤ ON Π.ΑΜΚΑ_ΠΡΟΣΩΠΙΚΟΥ = ΠΑΤ.ΑΜΚΑ
WHERE ΠΑΤ.ΟΝΟΜΑ_ΤΜΗΜΑΤΟΣ = 'Καρδιολογία'
  AND Π.ΑΜΚΑ_ΠΡΟΣΩΠΙΚΟΥ NOT IN (
      SELECT ΟΕ.ΑΜΚΑ_ΠΡΟΣΩΠΙΚΟΥ
      FROM ΟΜΑΔΑ_ΕΦΗΜΕΡΙΑΣ ΟΕ                    -- idx_omada_amka
      JOIN ΒΑΡΔΙΕΣ Β ON ΟΕ.ΚΩΔΙΚΟΣ_ΒΑΡΔΙΑΣ = Β.ΚΩΔΙΚΟΣ_ΒΑΡΔΙΑΣ  -- idx_vardies_tmima_date
      WHERE Β.ΗΜΕΡΟΜΗΝΙΑ = '2026-04-01'
        AND Β.ΟΝΟΜΑ_ΤΜΗΜΑΤΟΣ = 'Καρδιολογία'
        AND ΟΕ.ΑΜΚΑ_ΠΡΟΣΩΠΙΚΟΥ IS NOT NULL
  );