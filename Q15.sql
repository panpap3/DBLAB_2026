SELECT 
    Τ.ΕΠΙΠΕΔΟ_ΕΠΕΙΓΟΝΤΟΣ,
    Ν.ΟΝΟΜΑ_ΤΜΗΜΑΤΟΣ,
    COUNT(*) AS ΣΥΝΟΛΟ_ΠΕΡΙΣΤΑΤΙΚΩΝ,
    ROUND(AVG(
        GREATEST(0, TIMESTAMPDIFF(
            MINUTE, 
            Τ.ΗΜΕΡΟΜΗΝΙΑ_ΑΦΙΞΗΣ, 
            TIMESTAMP(Ν.ΗΜΕΡΟΜΗΝΙΑ_ΕΙΣΑΓΩΓΗΣ) + INTERVAL 12 HOUR
        ))
    ), 2) AS ΜΕΣΟΣ_ΧΡΟΝΟΣ_ΑΝΑΜΟΝΗΣ_ΛΕΠΤΑ,
    ROUND(COUNT(Ν.ΚΩΔΙΚΟΣ_ΝΟΣΗΛΕΙΑΣ) * 100.0 / 
        SUM(COUNT(*)) OVER (PARTITION BY Τ.ΕΠΙΠΕΔΟ_ΕΠΕΙΓΟΝΤΟΣ), 2) AS ΠΟΣΟΣΤΟ_ΠΑΡΑΠΟΜΠΩΝ
FROM ΤΜΗΜΑ_ΕΠΕΙΓΟΝΤΩΝ_ΠΕΡΙΣΤΑΤΙΚΩΝ Τ
JOIN ΝΟΣΗΛΕΙΑ Ν ON Τ.ΚΩΔΙΚΟΣ_ΠΕΡΙΣΤΑΤΙΚΟΥ = Ν.ΚΩΔΙΚΟΣ_ΠΕΡΙΣΤΑΤΙΚΟΥ
GROUP BY Τ.ΕΠΙΠΕΔΟ_ΕΠΕΙΓΟΝΤΟΣ, Ν.ΟΝΟΜΑ_ΤΜΗΜΑΤΟΣ
ORDER BY Τ.ΕΠΙΠΕΔΟ_ΕΠΕΙΓΟΝΤΟΣ, ΣΥΝΟΛΟ_ΠΕΡΙΣΤΑΤΙΚΩΝ DESC;