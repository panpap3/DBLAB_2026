SELECT 
    Π.ΟΝΟΜΑ,
    Π.ΕΠΩΝΥΜΟ,
    Ι.ΑΜΚΑ_ΙΑΤΡΟΥ,
    Ι.ΕΙΔΙΚΟΤΗΤΑ,
    CASE 
        WHEN EXISTS (
            SELECT 1 
            FROM ΟΜΑΔΑ_ΕΦΗΜΕΡΙΑΣ ΟΕ
            JOIN ΒΑΡΔΙΕΣ Β ON ΟΕ.ΚΩΔΙΚΟΣ_ΒΑΡΔΙΑΣ = Β.ΚΩΔΙΚΟΣ_ΒΑΡΔΙΑΣ
            WHERE ΟΕ.ΑΜΚΑ_ΠΡΟΣΩΠΙΚΟΥ = Ι.ΑΜΚΑ_ΙΑΤΡΟΥ 
              AND EXTRACT(YEAR FROM Β.ΗΜΕΡΟΜΗΝΙΑ) = EXTRACT(YEAR FROM CURRENT_DATE)
        ) THEN 'ΝΑΙ' 
        ELSE 'ΟΧΙ' 
    END AS ΕΙΧΕ_ΕΦΗΜΕΡΙΑ_ΦΕΤΟΣ,
    COUNT(CASE 
        WHEN ΙΠ.ΚΑΤΗΓΟΡΙΑ = 'ΠΡΑΞΕΙΣ ΧΕΙΡΟΥΡΓΙΚΕΣ – ΕΠΕΜΒΑΤΙΚΕΣ – ΕΝΔΟΣΚΟΠΙΚΕΣ' 
        THEN 1 
    END) AS ΣΥΝΟΛΟ_ΕΠΕΜΒΑΣΕΩΝ_ΩΣ_ΚΥΡΙΟΣ  -- μόνο ως κύριος χειρουργός
FROM ΙΑΤΡΟΙ Ι
JOIN ΠΡΟΣΩΠΙΚΟ Π ON Ι.ΑΜΚΑ_ΙΑΤΡΟΥ = Π.ΑΜΚΑ_ΠΡΟΣΩΠΙΚΟΥ
LEFT JOIN ΙΑΤΡΙΚΕΣ_ΠΡΑΞΕΙΣ_ΑΣΘΕΝΗ ΙΠΑ 
    ON Ι.ΑΜΚΑ_ΙΑΤΡΟΥ = ΙΠΑ.ΑΜΚΑ_ΙΑΤΡΟΥ  -- κύριος χειρουργός
LEFT JOIN ΙΑΤΡΙΚΕΣ_ΠΡΑΞΕΙΣ ΙΠ ON ΙΠΑ.ΚΩΔΙΚΟΣ_ΠΡΑΞΗΣ = ΙΠ.ΚΩΔΙΚΟΣ_ΠΡΑΞΗΣ
WHERE Ι.ΕΙΔΙΚΟΤΗΤΑ = 'Καρδιολόγος'
GROUP BY Π.ΟΝΟΜΑ, Π.ΕΠΩΝΥΜΟ, Ι.ΑΜΚΑ_ΙΑΤΡΟΥ, Ι.ΕΙΔΙΚΟΤΗΤΑ;