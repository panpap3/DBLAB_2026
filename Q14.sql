WITH Εισαγωγές_Ανά_Έτος AS (
    SELECT 
        SUBSTRING(Δ.ΚΩΔΙΚΟΣ_ICD10, 1, 3) AS ΚΑΤΗΓΟΡΙΑ_ICD10,
        EXTRACT(YEAR FROM Ν.ΗΜΕΡΟΜΗΝΙΑ_ΕΙΣΑΓΩΓΗΣ) AS ΕΤΟΣ,
        COUNT(*) AS ΑΡΙΘΜΟΣ_ΕΙΣΑΓΩΓΩΝ
    FROM ΔΙΑΓΝΩΣΕΙΣ_ΝΟΣΗΛΕΙΑΣ Δ
    JOIN ΝΟΣΗΛΕΙΑ Ν ON Δ.ΚΩΔΙΚΟΣ_ΝΟΣΗΛΕΙΑΣ = Ν.ΚΩΔΙΚΟΣ_ΝΟΣΗΛΕΙΑΣ
    WHERE Δ.ΤΥΠΟΣ = 'Εισόδου'
    GROUP BY SUBSTRING(Δ.ΚΩΔΙΚΟΣ_ICD10, 1, 3), EXTRACT(YEAR FROM Ν.ΗΜΕΡΟΜΗΝΙΑ_ΕΙΣΑΓΩΓΗΣ)
    HAVING COUNT(*) >= 5
)
SELECT 
    Ε1.ΚΑΤΗΓΟΡΙΑ_ICD10,
    Ε1.ΕΤΟΣ AS ΕΤΟΣ_1,
    Ε2.ΕΤΟΣ AS ΕΤΟΣ_2,
    Ε1.ΑΡΙΘΜΟΣ_ΕΙΣΑΓΩΓΩΝ
FROM Εισαγωγές_Ανά_Έτος Ε1
JOIN Εισαγωγές_Ανά_Έτος Ε2
    ON Ε1.ΚΑΤΗΓΟΡΙΑ_ICD10 = Ε2.ΚΑΤΗΓΟΡΙΑ_ICD10
    AND Ε2.ΕΤΟΣ = Ε1.ΕΤΟΣ + 1
    AND Ε1.ΑΡΙΘΜΟΣ_ΕΙΣΑΓΩΓΩΝ = Ε2.ΑΡΙΘΜΟΣ_ΕΙΣΑΓΩΓΩΝ
ORDER BY Ε1.ΚΑΤΗΓΟΡΙΑ_ICD10, Ε1.ΕΤΟΣ;